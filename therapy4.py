import sqlite3

def add_question(subject, question_text, options, correct_option, explanation):
    """
    Добавляет вопрос в базу данных.

    Параметры:
    - subject: название предмета (например: "histology", "pharmacology", "surgery", "therapy4")
    - question_text: текст вопроса
    - options: список вариантов ответа (от 2 до 5)
    - correct_option: номер правильного ответа (1-5)
    - explanation: пояснение к ответу
    """
    conn = sqlite3.connect("questions.db")
    cursor = conn.cursor()

    n = len(options)
    columns = [f"option{i+1}" for i in range(n)]

    query = f"""
        INSERT INTO questions (subject, question, {', '.join(columns)}, correct_option, explanation)
        VALUES ({', '.join(['?'] * (n + 4))})
    """

    cursor.execute(query, (subject, question_text, *options, correct_option, explanation))
    conn.commit()
    conn.close()
    print(f"Добавлен вопрос: {question_text[:50]}...")

